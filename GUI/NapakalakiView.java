    // MÃ©todo que da valor al atributo privado NapakalakiModel
    public void setNapakalaki(Napakalaki npk){
        this.currentMonster=npk.getCurrentMonster();
        this.currentPlayer=npk.getCurrentPlayer();
        this.NapakalakiModel=npk;
        this.playerView1.setNapakalaki(npk, this);
        this.playerView1.setPlayer(currentPlayer);
        
        if(this.MeetTheMonster.isEnabled() && this.combat.isEnabled() && this.NextTurn.isEnabled()){
            // Esto quiere decir que es la primera partida. Desactiva los botones correspondientes
            this.combat.setEnabled(false);
            this.NextTurn.setEnabled(false);
        }
        repaint();
        revalidate();
    }
    
    
    
